def Print_braile(String):
        String = String.upper()
         
        print(String)
        
        index=[]
       # String=String.replace(" ", "")
        String=String.replace("\n", "")
        
        n = len(String)
        
        for char in String:
            idx=alphabets.index(char)
            index.append(idx)
            
        k=0    
        for j in range(0,3):
            for i in range(0,n):
                l= index[i]
                if(matrix[l][k] == 1):
                    file2.write("o ")
                
                elif(matrix[l][k] == 2):
                    file2.write("  ")
               
                else:
                    file2.write(". ")
                
                if(matrix[l][k+1] == 1):
                        file2.write("o ")
                
                elif(matrix[l][k] == 2):
                    file2.write("  ")
               
                else:
                    file2.write(". ")
                file2.write("  ",)
            k=k+2
                
            file2.write("\n")
        file2.write("\n")
            
